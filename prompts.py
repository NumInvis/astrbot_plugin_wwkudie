def build_article_prompt(message):
    parts = message.strip().split(maxsplit=1)
    game_name = parts[0] if parts else "游戏"
    event = parts[1] if len(parts) > 1 else "相关事件"
    return f"游戏：{game_name}\n事件：{event}\n\n请根据上述游戏和事件，撰写一篇符合风格要求的深度评论文章。"
